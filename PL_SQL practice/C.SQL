DECLARE
	N NUMBER(4) :=&N;
	C NUMBER(4) :=0;
BEGIN
	IF N > 100 THEN
		DBMS_OUTPUT.PUT_LINE('N > 100');
	ELSE
		DBMS_OUTPUT.PUT_LINE('N < 100');
	END IF;

	IF N < 100 THEN
		DBMS_OUTPUT.PUT_LINE('N < 100');
	ELSIF N >= 100 AND N <= 200 THEN
		DBMS_OUTPUT.PUT_LINE('N BETWEEN 100 AND 200');
	ELSE
		DBMS_OUTPUT.PUT_LINE('N > 200');
	END IF;

	CASE N
		WHEN 100 THEN
			DBMS_OUTPUT.PUT_LINE('N = 100');
		WHEN 200 THEN
			DBMS_OUTPUT.PUT_LINE('N = 200');
		ELSE
			DBMS_OUTPUT.PUT_LINE('N != 100 AND N != 200');
	END CASE;

	CASE
		WHEN N < 100 THEN
			DBMS_OUTPUT.PUT_LINE('N < 100');
		WHEN N < 200 THEN
			DBMS_OUTPUT.PUT_LINE('N > 100 AND N < 200');
		ELSE
			DBMS_OUTPUT.PUT_LINE('N > 100');
	END CASE;

	FOR I IN 1..5 LOOP
		DBMS_OUTPUT.PUT_LINE(I);
	END LOOP;

	FOR I IN REVERSE 1..5 LOOP
		DBMS_OUTPUT.PUT_LINE(I);
	END LOOP;

	WHILE C <= 5 LOOP
		DBMS_OUTPUT.PUT_LINE(C);
		C:=C+1;
	END LOOP;

	LOOP
		DBMS_OUTPUT.PUT_LINE(C);
		C:=C+1;
		EXIT WHEN C=10;
	END LOOP;
END;
