DECLARE
	N NUMBER(4):=&N;
BEGIN
	IF N < 1000 THEN
		DBMS_OUTPUT.PUT_LINE(N);
	ELSE
		RAISE_APPLICATION_ERROR(-20567, 'NUMBER > 1000 NOT ALLOWED');
	END IF;
EXCEPTION
	WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE(SQLCODE||' '||SQLERRM);
		RAISE;
END;
