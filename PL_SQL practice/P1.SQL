DECLARE 
	CURSOR C1(DNO NUMBER) IS SELECT * FROM EMP WHERE DEPTNO=DNO;
	X EMP%ROWTYPE;
BEGIN 
	OPEN C1(&DNO);
	LOOP
		FETCH C1 INTO X;
		EXIT WHEN C1%NOTFOUND;
		DBMS_OUTPUT.PUT_LINE(X.ENAME||' '||X.SAL||' '||X.JOB);
	END LOOP;
	DBMS_OUTPUT.PUT_LINE('RECORD FETCH::'||C1%ROWCOUNT);
	CLOSE C1;
END;
