DECLARE
	TYPE T IS TABLE OF NUMBER;
	R T:=T();
BEGIN
	R.EXTEND();
	R(1):=2000;
	R.EXTEND();
	R(2):=3000;
	R.EXTEND();
	R(3):=4000;
	R.EXTEND();
	R(4):=5000;

	FOR I IN 1..4 LOOP
		DBMS_OUTPUT.PUT_LINE(R(I));
	END LOOP;

	DBMS_OUTPUT.PUT_LINE(R(R.FIRST));
	DBMS_OUTPUT.PUT_LINE(R(R.LAST));
	DBMS_OUTPUT.PUT_LINE(R.COUNT);
	R.DELETE(1);
	DBMS_OUTPUT.PUT_LINE(R.COUNT);
END;
/