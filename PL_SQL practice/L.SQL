DECLARE
	TYPE T IS VARRAY(5) OF NUMBER;
	R T:=T(11, 22, 33, 44, 55);
BEGIN
	FOR I IN 1..4 LOOP
		DBMS_OUTPUT.PUT_LINE(R(I));
	END LOOP;

	DBMS_OUTPUT.PUT_LINE(R(R.FIRST));
	DBMS_OUTPUT.PUT_LINE(R(R.LAST));
	DBMS_OUTPUT.PUT_LINE(R.COUNT);
	R.DELETE();
	DBMS_OUTPUT.PUT_LINE(R.COUNT);
END;
/