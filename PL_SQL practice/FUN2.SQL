CREATE OR REPLACE PACKAGE MYPKG
IS
	PROCEDURE A;
	FUNCTION GETTIME RETURN VARCHAR2;
END;
/

CREATE OR REPLACE PACKAGE BODY MYPKG
IS
	PROCEDURE B
	IS
	BEGIN
		DBMS_OUTPUT.PUT_LINE('HI THIS IS A PRIVATE PROCEDURE');
	END;

	PROCEDURE B
	(NM VARCHAR2)
	IS
	BEGIN
		DBMS_OUTPUT.PUT_LINE('HI THIS IS A PRIVATE PROCEDURE OVERRIDING THE ORIGINAL, PASSED VALUE -> '||NM);
	END;

	PROCEDURE A
	IS
	BEGIN
		B();
		DBMS_OUTPUT.PUT_LINE('HI THIS IS A PUBLIC PROCEDURE');
		B('BLAZ');
	END;

	FUNCTION GETTIME RETURN VARCHAR2
	IS
	BEGIN
		RETURN TO_CHAR(SYSDATE, 'HH:MI:SS');
	END;
END;
/